@pytest.fixture
def client():
    import data_analysis_agent.llm.client as llm_module
    llm_module._client = None
    from data_analysis_agent.api import create_app
    with TestClient(create_app(), raise_server_exceptions=True) as c:
        yield c


def _csv(*rows) -> bytes:
    buf = io.StringIO()
    w = csv.writer(buf)
    for r in rows:
        w.writerow(r)
    return buf.getvalue().encode()


def _orders():
    return _csv(["id", "amount"], [1, 5], [2, 7])


def _only_dataset_id() -> str:
    with session_module.create_db_session() as db:
        return db.query(DataSourceRow).one().id


def test_upload_creates_dataset_and_one_table(client):
    r = client.post("/datasources/upload", data={"dataset_name": "Sales DB"},
                    files={"file": ("orders.csv", _orders(), "text/csv")}, follow_redirects=True)
    assert r.status_code == 200
    with session_module.create_db_session() as db:
        ds = db.query(DataSourceRow).one()
        assert ds.name == "Sales DB" and ds.type == "parquet"
        assert ds.dataset_uri == "parquet:///Sales%20DB"
        assert ds.tool_description  # generated by the stub
        tables = db.query(DatasetTableRow).filter_by(dataset_id=ds.id).all()
        assert len(tables) == 1
        assert tables[0].table_name == "orders"
        assert tables[0].parquet_path and "orders.parquet" in tables[0].parquet_path
        assert tables[0].capability_description


def test_add_csv_appends_a_table(client):
    client.post("/datasources/upload", data={"dataset_name": "DB"},
                files={"file": ("orders.csv", _orders(), "text/csv")}, follow_redirects=True)
    ds_id = _only_dataset_id()
    r = client.post(f"/datasources/{ds_id}/add-csv",
                    files={"file": ("customers.csv", _csv(["id", "region"], [1, "N"]), "text/csv")},
                    follow_redirects=True)
    assert r.status_code == 200
    with session_module.create_db_session() as db:
        tables = {t.table_name for t in db.query(DatasetTableRow).filter_by(dataset_id=ds_id).all()}
        assert tables == {"orders", "customers"}


def test_duplicate_dataset_name_rejected(client):
    client.post("/datasources/upload", data={"dataset_name": "Dupe"},
                files={"file": ("a.csv", _orders(), "text/csv")}, follow_redirects=True)
    r = client.post("/datasources/upload", data={"dataset_name": "Dupe"},
                    files={"file": ("b.csv", _orders(), "text/csv")}, follow_redirects=False)
    assert r.status_code == 400


def test_upload_requires_dataset_name(client):
    r = client.post("/datasources/upload",
                    files={"file": ("a.csv", _orders(), "text/csv")}, follow_redirects=False)
    assert r.status_code == 400


def test_external_uri_returns_501_when_disabled(client):
    r = client.post("/datasources/upload",
                    data={"dataset_name": "PG", "dataset_type": "postgresql",
                          "dataset_uri": "postgresql://u:p@h:5432/db"},
                    follow_redirects=False)
    assert r.status_code == 501


def test_delete_removes_directory_and_rows(client):
    client.post("/datasources/upload", data={"dataset_name": "ToDelete"},
                files={"file": ("orders.csv", _orders(), "text/csv")}, follow_redirects=True)
    ds_id = _only_dataset_id()
    with session_module.create_db_session() as db:
        table = db.query(DatasetTableRow).filter_by(dataset_id=ds_id).one()
        dataset_dir = Path(table.parquet_path).parent
    assert dataset_dir.exists()

    r = client.post(f"/datasources/{ds_id}/delete", follow_redirects=True)
    assert r.status_code == 200
    with session_module.create_db_session() as db:
        assert db.query(DataSourceRow).count() == 0
        assert db.query(DatasetTableRow).count() == 0
    assert not dataset_dir.exists()

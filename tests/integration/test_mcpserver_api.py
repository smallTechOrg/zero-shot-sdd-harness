def test_jsonrpc_tools_list_call_and_errors(client):
    _create(client, "RPC DB")
    server_id, _, _ = _only_server()

    listed = client.post(f"/mcpserver/{server_id}",
                         json={"jsonrpc": "2.0", "id": 1, "method": "tools/list"})
    assert listed.status_code == 200
    body = listed.json()
    tools = body["result"]["tools"]
    assert tools and "inputSchema" in tools[0]
    tool_name = tools[0]["name"]

    called = client.post(f"/mcpserver/{server_id}",
                         json={"jsonrpc": "2.0", "id": 2, "method": "tools/call",
                               "params": {"name": tool_name, "arguments": {}}}).json()
    assert called["result"]["isError"] is False
    assert called["result"]["content"][0]["type"] == "text"

    # resources + prompts also list
    res = client.post(f"/mcpserver/{server_id}",
                      json={"jsonrpc": "2.0", "id": 3, "method": "resources/list"}).json()
    assert any(r["uri"].endswith("/schema") for r in res["result"]["resources"])

    # unknown method → -32601, unknown tool → -32602
    unk = client.post(f"/mcpserver/{server_id}",
                      json={"jsonrpc": "2.0", "id": 4, "method": "nope/x"}).json()
    assert unk["error"]["code"] == -32601
    bad = client.post(f"/mcpserver/{server_id}",
                      json={"jsonrpc": "2.0", "id": 5, "method": "tools/call",
                            "params": {"name": "does_not_exist", "arguments": {}}}).json()
    assert bad["error"]["code"] == -32602



def _csv(*rows) -> bytes:
    buf = io.StringIO()
    w = csv.writer(buf)
    for r in rows:
        w.writerow(r)
    return buf.getvalue().encode()


def _orders() -> bytes:
    return _csv(["id", "amount"], [1, 5], [2, 7])


def _create(client, name: str, data: bytes | None = None, filename: str = "orders.csv"):
    up = client.post("/mcpserver/upload", data={"dataset_name": name},
                     files={"file": (filename, data or _orders(), "text/csv")})
    assert up.status_code == 200, up.text
    uri = up.json()["uri"]
    return client.post("/mcpserver", data={"name": name, "dataset_type": "parquet", "dataset_uri": uri},
                       follow_redirects=True)


def _only_server():
    with session_module.create_db_session() as db:
        srv = db.query(McpServerRow).one()
        return srv.id, srv.version, len(srv.physical_tables)


def test_create_builds_server_and_capabilities(client):
    r = _create(client, "Sales DB")
    assert r.status_code == 200
    with session_module.create_db_session() as db:
        srv = db.query(McpServerRow).one()
        assert srv.name == "Sales DB" and srv.type == "parquet"
        assert srv.uri == "parquet:///Sales%20DB"
        assert srv.title and srv.description           # generated by the sync stub
        assert srv.version >= 2                         # create(1) + sync apply bumps to 2
        assert len(srv.physical_tables) == 1
        tools = db.query(McpToolRow).filter_by(server_id=srv.id, deleted_at=None).all()
        assert len(tools) >= 1


